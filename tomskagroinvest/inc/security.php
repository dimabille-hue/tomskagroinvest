<?php

if (!defined('ABSPATH')) {
	exit;
}

/*
|--------------------------------------------------------------------------
| SVG
|--------------------------------------------------------------------------
*/

add_filter(
	'upload_mimes',
	function ($mimes) {

		$mimes['svg'] = 'image/svg+xml';

		return $mimes;
	}
);

/*
|--------------------------------------------------------------------------
| XML-RPC
|--------------------------------------------------------------------------
*/

add_filter(
	'xmlrpc_enabled',
	'__return_false'
);

/*
|--------------------------------------------------------------------------
| REST API для неавторизованных
|--------------------------------------------------------------------------
*/

add_filter(
	'rest_authentication_errors',
	function ($result) {

		if (!empty($result)) {

			return $result;
		}

		return $result;
	}
);

/*
|--------------------------------------------------------------------------
| Ограничение ревизий
|--------------------------------------------------------------------------
*/

if (!defined('WP_POST_REVISIONS')) {

	define(
		'WP_POST_REVISIONS',
		10
	);
}